import { ViewletCommand } from '@lvce-editor/constants'
import type { ChatState } from '../ChatState/ChatState.ts'
import { getChatVirtualDom } from '../GetChatViewDom/GetChatViewDom.ts'

export const renderItems = (oldState: ChatState, newState: ChatState): any => {
  const {
    addContextButtonEnabled,
    agentMode,
    agentModePickerOpen,
    authEnabled,
    authErrorMessage,
    chatListScrollTop,
    composerAttachmentPreviewOverlayAttachmentId,
    composerAttachmentPreviewOverlayError,
    composerAttachments,
    composerDropActive,
    composerDropEnabled,
    composerFontFamily,
    composerFontSize,
    composerHeight,
    composerLineHeight,
    composerValue,
    gitBranches,
    gitBranchPickerErrorMessage,
    gitBranchPickerOpen,
    gitBranchPickerVisible,
    hasSpaceForAgentModePicker,
    hasSpaceForRunModePicker,
    initial,
    listFocusedIndex,
    listFocusOutline,
    messagesAutoScrollEnabled,
    messagesScrollTop,
    modelPickerOpen,
    modelPickerSearchValue,
    models,
    openApiApiKeyInput,
    openApiApiKeyInputPattern,
    openApiApiKeysSettingsUrl,
    openApiApiKeyState,
    openRouterApiKeyInput,
    openRouterApiKeyState,
    parsedMessages,
    projectExpandedIds,
    projectListScrollTop,
    projects,
    reasoningEffort,
    reasoningEffortPickerOpen,
    reasoningPickerEnabled,
    runMode,
    runModePickerOpen,
    scrollDownButtonEnabled,
    searchEnabled,
    searchFieldVisible,
    searchValue,
    selectChevronEnabled,
    selectedModelId,
    selectedProjectId,
    selectedSessionId,
    selectedSessionViewModel,
    sessions,
    showChatListTime,
    showRunMode,
    todoListToolEnabled,
    tokensMax,
    tokensUsed,
    uid,
    usageOverviewEnabled,
    useChatMathWorker,
    userName,
    userState,
    viewMode,
    visibleModels,
    voiceDictationEnabled,
  } = newState
  if (initial) {
    return [ViewletCommand.SetDom2, uid, []]
  }
  const dom = getChatVirtualDom({
    addContextButtonEnabled,
    agentMode,
    agentModePickerOpen,
    authEnabled,
    authErrorMessage,
    chatListScrollTop,
    composerAttachmentPreviewOverlayAttachmentId,
    composerAttachmentPreviewOverlayError,
    composerAttachments,
    composerDropActive,
    composerDropEnabled,
    composerFontFamily,
    composerFontSize,
    composerHeight,
    composerLineHeight,
    composerValue,
    gitBranches,
    gitBranchPickerErrorMessage,
    gitBranchPickerOpen,
    gitBranchPickerVisible,
    hasSpaceForAgentModePicker,
    hasSpaceForRunModePicker,
    listFocusedIndex,
    listFocusOutline,
    messagesAutoScrollEnabled,
    messagesScrollTop,
    modelPickerOpen,
    modelPickerSearchValue,
    models,
    openApiApiKeyInput,
    openApiApiKeyInputPattern,
    openApiApiKeysSettingsUrl,
    openApiApiKeyState,
    openRouterApiKeyInput,
    openRouterApiKeyState,
    parsedMessages,
    projectExpandedIds,
    projectListScrollTop,
    projects,
    reasoningEffort,
    reasoningEffortPickerOpen,
    reasoningPickerEnabled,
    runMode,
    runModePickerOpen,
    scrollDownButtonEnabled,
    searchEnabled,
    searchFieldVisible,
    searchValue,
    selectChevronEnabled,
    selectedModelId,
    selectedProjectId,
    selectedSessionId,
    ...(selectedSessionViewModel
      ? {
          selectedSessionViewModel,
        }
      : {}),
    sessions,
    showChatListTime,
    showRunMode,
    todoListToolEnabled,
    tokensMax,
    tokensUsed,
    usageOverviewEnabled,
    useChatMathWorker,
    userName,
    userState,
    viewMode,
    visibleModels,
    voiceDictationEnabled,
  })
  return [ViewletCommand.SetDom2, uid, dom]
}
